/** Translate a single BGG category or mechanic to German. Falls back to original. */
export function translateCategory(en: string): string {
  return CATEGORIES[en] ?? en;
}

export function translateMechanic(en: string): string {
  return MECHANICS[en] ?? en;
}

/** Translate an array (categories or mechanics). */
export function translateCategories(arr: string[] | null | undefined): string[] {
  return (arr ?? []).map(translateCategory);
}

export function translateMechanics(arr: string[] | null | undefined): string[] {
  return (arr ?? []).map(translateMechanic);
}
